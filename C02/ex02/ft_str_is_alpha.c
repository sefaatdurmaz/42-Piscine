#include <unistd.h>
#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	x;

	x = 0;
	while (str[x] != '\0')
	{
		if ((str[x] >= 65 && str[x] <= 90) || (str[x] >= 97 && str[x] <= 122))
		{
			x++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
